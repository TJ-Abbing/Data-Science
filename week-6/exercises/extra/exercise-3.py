"""
Exercise 3: Creating and Modifying Sets
"""
# Create an empty set called "my_set".
my_set = []

# Add the numbers 1, 2, and 3 to "my_set".
my_set.append(1)
my_set.append(2)
my_set.append(3)

# Add the number 3 to "my_set" again.
my_set.append(3)

# Remove the number 2 from "my_set".
my_set.remove(2)

# Print the entire set.
print(my_set)